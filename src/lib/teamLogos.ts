// Funci√≥n para obtener logo de equipo
export function getTeamLogo(teamName: string): string {
  // Buscar logo exacto
  if (TEAM_LOGOS[teamName]) {
    return TEAM_LOGOS[teamName]
  }

  // Buscar logo con coincidencia parcial
  const lowerTeamName = teamName.toLowerCase()
  for (const [key, logo] of Object.entries(TEAM_LOGOS)) {
    if (key.toLowerCase().includes(lowerTeamName) || lowerTeamName.includes(key.toLowerCase())) {
      return logo
    }
  }

  // Fallback a emoji basado en el deporte o nombre
  const fallbackEmojis: Record<string, string> = {
    'football': '‚öΩ',
    'soccer': '‚öΩ',
    'basketball': 'üèÄ',
    'baseball': '‚öæ',
    'american football': 'üèà'
  }

  // Si no se encuentra, usar emoji gen√©rico
  return fallbackEmojis['football'] || '‚öΩ'
}

// Funci√≥n para verificar si es una URL de imagen
export function isImageUrl(url: string): boolean {
  return url.startsWith('http') && (url.includes('.png') || url.includes('.jpg') || url.includes('.jpeg') || url.includes('.svg') || url.includes('.webp'))
}